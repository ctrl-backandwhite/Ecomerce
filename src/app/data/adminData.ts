/* ── Shared admin data (categories, stores, etc.) ───────── */

export interface Category {
  id: string;
  name: string;
  slug: string;
  icon?: string;          // Lucide icon name, e.g. "Cpu", "Shirt" (only for parent categories)
  description: string;
  keywords: string[];
  parent_id: string | null;
  productCount: number;
  status: "active" | "inactive";
  published: boolean;     // Draft vs Published
  order: number;         // Display order
}

export interface Subcategory {
  id: string;
  name: string;
  slug: string;
  productCount: number;
  status: "active" | "inactive";
  order: number;         // Display order within category
}

